package Notes_and_Resources.Programming_Languages.Java.Java_Tutorial_for_Beginners_1;

public class Main {

    public static void main(String[] args) {
        
        //takeaway: difference between print and println
        System.out.println("I am Cristiano");
        System.out.println("I love java");

        //takeaway: \n same function as println
        System.out.print("I am Cristiano\n"); 
        
        System.out.println("\"I love python also\""); //print ""
        System.out.println("\\I love JavaScript also"); //print \
        
        /*
         * Hello
         * I am Cristiano
         * I love Java
         * I love Python also
         */

    }
    
}
